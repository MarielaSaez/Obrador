    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       int fila = jtMovimientos.getSelectedRow();

    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Por favor, selecciona una fila.");
        return;
    }

    try {
        int id = (int) jtMovimientos.getValueAt(fila, 0);

        if (jtMovimientos.getValueAt(fila, 4) != null) {
            int choice = JOptionPane.showConfirmDialog(
                this,
                "¿Seguro desea modificar la fecha de devolución por la actual?",
                "Confirmación",
                JOptionPane.YES_NO_OPTION
            );

            if (choice == JOptionPane.YES_OPTION) {
                Movimiento m = new Movimiento(
                    id,
                    0,
                    null,
                    null,
                    null,
                    LocalDate.now()  
                );

                movimientoData.registrarDevolucion(m);
                JOptionPane.showMessageDialog(this, "Fecha de devolución registrada");
            }
        } else {
            Movimiento m = new Movimiento(
                id,
                0,
                null,
                null,
                null,
                LocalDate.now()  
            );

            movimientoData.registrarDevolucion(m);
            JOptionPane.showMessageDialog(this, "Fecha de devolución registrada");
        }

        dispose();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "La excepción debe ser un número");
    }

    }//GEN-LAST:event_jButton1ActionPerformed


