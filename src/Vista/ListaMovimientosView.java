private void armarLista(){
    DateTimeFormatter formato=DateTimeFormatter.ofPattern("dd/MM/YYYY");
    
    modelo.addColumn("ID");
    modelo.addColumn("Empleado");
    modelo.addColumn("Herramienta");
    modelo.addColumn("Fecha de pedido");
    modelo.addColumn("Fecha de devolución");
    modelo.addColumn("Cantidad");
    for(Movimiento m: movimientoData.obtenerMovimientos()){
               
        modelo.addRow(new Object[] {
            m.getId_movimiento(),
            m.getEmpleado().toString(),
            m.getHerramienta().toString(),
            m.getFechap().format(formato),
            m.getFechad().format(formato),
            m.getCantidad()});        
        }
    jtMovimientos.setModel(modelo);
}
