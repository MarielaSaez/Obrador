    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int cant=0;
        if(jtCantidad.getText().isEmpty()){
           JOptionPane.showMessageDialog(this, "Debe asignar una cantidad");
           return;
        }
        try{
        cant=Integer.parseInt(jtCantidad.getText());
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "La cantidad debe ser un numero");
        }
        Movimiento m=new Movimiento(cant,(Empleado)cbEmpleado.getSelectedItem(),
                (Herramienta) cbHerramienta.getSelectedItem(),jcFechap.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate(),null);
        md.guardarMovimiento(m);
        limpiar();
        JOptionPane.showMessageDialog(this, "Movimiento cargado");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void limpiar(){
        jtCantidad.setText("");
        jcFechap.setDate(new Date());
        cbEmpleado.setSelectedIndex(0);
        cbHerramienta.setSelectedIndex(0);
    }
